celsius = float(input("Enter temperature in Celsius: "))
#Conversion formulae = fahrenheit = (celsius * 9/5) + 32
fahrenheit = (celsius * 9/5) + 32
print(f"{celsius} degree celsius is equal to {fahrenheit} degree fahrenheit")

